#include <iostream>
int main()
{
    using namespace std;
    int i;
    for(i=0;i<5;i++)
        cout<<"C++ knows loops.\n";
    cout<<"C++ knows when to stop.\n";
    return 0;
}

/** 本段代码小结
 * for 循环的使用
 * i=0 进行初始化
 * 1<5是循环测试部分，检查i是否小于5
 * 如果满足执行循环体  cout<<"C++ knows loops.\n";
 * 然后使用循环更新i++
 * */

